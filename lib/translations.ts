// Helper para obtener traducciones
export function getTranslation(lang: 'es' | 'en', key: string, params?: Record<string, string | number>) {
  const text = TRANSLATIONS[lang]?.[key] || key
  if (params) {
    return text.replace(/{(\w+)}/g, (_, k) => String(params[k] || ''))
  }
  return text
}